// Componente principal que selecciona la bandera según el país
export function CountryFlag({ country }: { country: string }) {
  switch (country) {
    case "Perú":
      return <PeruFlag />;
    case "Ecuador":
      return <EcuadorFlag />;
    case "Colombia":
      return <ColombiaFlag />;
    case "Panamá":
      return <PanamaFlag />;
    case "Costa Rica":
      return <CostaRicaFlag />;
    case "Rep. Dominicana":
      return <DominicanRepublicFlag />;
    case "El Salvador":
      return <ElSalvadorFlag />;
    case "Guatemala":
      return <GuatemalaFlag />;
    case "México":
      return <MexicoFlag />;
    default:
      return <PeruFlag />;
  }
}
